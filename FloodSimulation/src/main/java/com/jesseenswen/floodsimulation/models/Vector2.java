package com.jesseenswen.floodsimulation.models;

/**
 *
 * @author swenm_000
 */
public class Vector2<T> {

    private T x;
    private T y;

    public Vector2() { }

    public Vector2(T x, T y) {
        this.x = x;
        this.y = y;
    }

    public T getX() {
        return x;
    }

    public void setX(T x) {
        this.x = x;
    }

    public T getY() {
        return y;
    }

    public void setY(T y) {
        this.y = y;
    }
}
